package builder;

public class Car {
private String mat;
private int seats;
private boolean gps;
public String getMat() {
	return mat;
}
public void setMat(String mat) {
	this.mat = mat;
}
public int getSeats() {
	return seats;
}
public void setSeats(int seats) {
	this.seats = seats;
}
public boolean isGps() {
	return gps;
}
public void setGps(boolean gps) {
	this.gps = gps;
}


}
